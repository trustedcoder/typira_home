def get_semantic_hash(sentence):
    """
    Returns a 64-bit SimHash (as hex) of the normalized intent of the sentence.
    No cloud API calls required.
    """
    if not sentence:
        return None
    
    # 1. Normalization: Lowercase and strip punctuation
    clean = re.sub(r'[^\w\s]', '', sentence.lower())
    
    # 2. Tokenization: Filter out stop words to find 'Intent'
    tokens = [w for w in clean.split() if w not in STOP_WORDS]
    
    # Fallback to full tokens if it's a very short sentence
    if not tokens:
        tokens = clean.split()
    
    if not tokens:
        return None
        
    # 3. SimHash: Generate a locality-sensitive hash of the intent
    # Note: simhash-py returns a 64-bit integer
    hash_obj = Simhash(tokens)
    
    # Return as 16-character hex string for stable DB storage
    return format(hash_obj.value, '016x')
